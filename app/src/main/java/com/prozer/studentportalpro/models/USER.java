package com.prozer.studentportalpro.models;

public class User {

    private int id;
    private String name;
    private String email;
    private String phone;
    private String website;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getPhone() {
        return phone;
    }

    public String getWebsite() {
        return website;
    }
}